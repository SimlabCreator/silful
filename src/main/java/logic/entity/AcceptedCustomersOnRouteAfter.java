package logic.entity;

public class AcceptedCustomersOnRouteAfter {
	
	private int after;
	private int route;

	public AcceptedCustomersOnRouteAfter() {

	}
	
	public AcceptedCustomersOnRouteAfter(int after, int route) {
		this.after = after;
		this.route = route;
	}
	
	
	public int getAfter() {
		return after;
	}
	
	public void setAfter(int after) {
		this.after = after;
	}
	
	
	public int getRoute() {
		return route;
	}
	
	public void setRoute(int route) {
		this.route = route;
	}
	
	
	


}
